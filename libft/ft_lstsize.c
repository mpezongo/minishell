#include "../minishell.h"

int	ft_lstsize(t_lexer *lst)
{
	int	i;

	i = 0;
	if (lst != NULL)
	{
		while (lst != NULL)
		{
			i++;
			lst = lst->next;
		}
	}
	return (i);
}
